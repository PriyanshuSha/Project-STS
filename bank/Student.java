package first.bank;

public class Student {

	int StudentId;
	int StudentAge;
	int StudentMarks;
	String StudentName;
	public Student(int StudentId, int StudentAge, int StudentMarks, String StudentName) {
		
		this.StudentId = StudentId;
		this.StudentAge = StudentAge;
		this.StudentMarks = StudentMarks;
		this.StudentName = StudentName;
	}
}
